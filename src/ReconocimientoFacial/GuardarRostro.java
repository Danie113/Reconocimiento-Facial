    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre, apellidos, dni, direccion;
        nombre = txtNombre.getText();
        apellidos = txtApellido.getText();
        dni = txtDni.getText();
        direccion = txtDireccion.getText();

        if (nombre.length() == 0 || apellidos.length() == 0 || dni.length() == 0 || direccion.length() == 0) {
            JOptionPane.showMessageDialog(this, "RELLENE TODOS LOS DATOS");
            return;
        }
        try {
            File directorio = new File("data\\" + dni + "-" + nombre + "-" + apellidos + "-" + direccion);
            if (!directorio.exists()) {
                if (directorio.mkdirs()) {
                    System.out.println("Directorio creado");
                } else {
                    System.out.println("Error al crear directorio");
                }
            }
            g = jPanel1.getGraphics();
            double w = frame.width();
            double h = frame.height();
            faceDetector.detectMultiScale(frame, rostros, 1.3, 2, 0 | CASCADE_SCALE_IMAGE, new Size(30, 30), new Size(w, h));
            facesArray = rostros.toArray();
            for (int j = 0; j < 100; j++) {
                if (facesArray.length > 0) {
                    Mat faceROI = new Mat();
                    for (int i = 0; i < facesArray.length; i++) {
                        faceROI = frame.submat(facesArray[i]);
                    }
                    Imgcodecs.imencode(".bmp", faceROI, mem);
                    Image im = ImageIO.read(new ByteArrayInputStream(mem.toArray()));
                    buff = (BufferedImage) im;
                    File image = new File(directorio + "\\" + j + ".png");
                    ImageIO.write(buff, "png", image);
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(GuardarRostro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

